void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart)
{ 
    volatile temp_buffer_t temp_dma;
    if(huart == &DBG_HUART)
    {
        // pop from the fifo 
        temp_dma = BVR_fifo_pop_from_temp(&dbg_uart_tx_fifo);
        // Send to the DMA
        HAL_UART_Transmit_DMA(  &DBG_HUART, 
                                temp_dma.p_temp_buff, 
                                temp_dma.buff_size);
    }
}





